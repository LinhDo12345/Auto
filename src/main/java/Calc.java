public  class Calc {
    public  int  sum(int number1, int number2, int number3)  {
        return number1 + number2 + number3;
    }

    public  int  sum(int number1, int number2)  {
        return number1 + number2;
    }

    public  float  sum(float number1, float number2)  {
        return number1 + number2;
    }
}
